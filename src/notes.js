/*📘 Server-Side Rendering (SSR)
📌 Definition:

 Server এ HTML generate হয়ে browser এ পাঠানো হয়

 How it works:
User request পাঠায়
Server data fetch করে
HTML তৈরি করে
Browser এ পাঠায়
✅ Features:
SEO friendly ✔️
First load fast (content ready থাকে) ✔️
Slow device এও ভালো কাজ করে ✔️
❌ Limitations:
প্রতি request এ server load বাড়ে ❌
একটু slow হতে পারে (data fetch time) ❌
*/

